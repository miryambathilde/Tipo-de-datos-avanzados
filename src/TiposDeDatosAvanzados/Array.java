package TiposDeDatosAvanzados;

public class Array {
    public static void main(String[] args) {

        // ARRAY DE ENTEROS

        // DECLARAMOS E INICIALIZAMOS EL ARRAY
        int arrayUno[] = { 1, 2, 3, 4, 5};
        // RECORREMOS EL ARRAY
        for (int i : arrayUno) {
            System.out.println(i);
        }
    }
}
